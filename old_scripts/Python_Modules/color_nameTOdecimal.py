
#color name to decimal conversion
def nameTOdecimal(name):
    if (name=="red"):
        return (255,0,0)
    elif (name == "black"):
        return (0,0,0)
    elif (name == "coral"):
        return (255,127,80)
    elif (name == "tomato"):
        return (255,99,71)
    elif (name == "lime"):
        return (0,255,0)
    elif (name == "darkgreen"):
        return (0,100,0)
    elif (name == "cyan"):
        return (0,255,255)
    elif (name == "blue"):
        return (0,0,255)
